                <h2 class="card-title">Resumen</h2>
                <img src="/Img/C-Sharp.png" width="40%" heigth="40%" align="center" class="imag" alt="...">
                <br>
                <p align="justify" class="text">La Materia de Tópicos avanzados de Programación se divide en 5 unidades
                    y aborda temáticas como: En la primera unidad se estudia la Interfaz gráfica de usuario (GUI),
                    dependiendo de las herramientas utilizadas en los cursos anteriores se puede elegir la misma
                    herramienta o una distinta con el fin de que el estudiante aprenda a utilizar los componentes
                    gráficos que brinde el entorno de desarrollo, que incluya el manejo de eventos y que desarrolle
                    nuevos componentes derivados de los ya existentes pero que incluyan nuevas propiedades. En la
                    segunda unidad se enfoca al estudio de la creación y manejo de librerías y componentes en el
                    lenguaje de C# se entiende como librería a un conjunto de bloques de códigos de programación
                    normalmente compilados, que pueden ser incluidos en una aplicación para su uso. Y a un componente
                    como una clase de uso específico, lista para usar, que puede ser configurada o utilizada de forma
                    visual, desde el entorno de desarrollo. Esta unidad debe enfatizar la creación de nuevas librerías y
                    componentes y evitar el estudio exhaustivo de las que incluya el entorno de desarrollo, a estas
                    últimas enfocarse solo en revisar la forma de utilizarlos. En la tercera unidad se aborda un tema
                    complicado por lo que requiere de un tiempo razonable, el tema de programación concurrente requiere
                    de iniciar con el estudio a nivel conceptual sobre los hilos y su funcionamiento, y después ir
                    implementando aplicaciones multihilos. Uno de los puntos más sensibles es la sincronización por lo
                    que deben hacerse hincapié en una buena implementación. Para esta unidad es recomendable hacer
                    prácticas sencillas para comprender la funcionalidad de los hilos, el manejo de sus estados y la
                    sincronización, para finalmente desarrollar aplicaciones que usen la programación concurrente en la
                    resolución de problemas reales. En la cuarta unidad se aborda un tema complicado por lo cual se
                    requiere iniciar con el estudio de cuáles son los diferentes manejadores de Base de Datos que existe
                    en la actualizad, sus ventajas y sus desventajas, así mismo la conexión que maneja, por lo cual el
                    alumno debe desarrollar la habilidad de Establecer conexiones a diferentes orígenes de datos para su
                    manipulación y visualización de información. Finalmente, La quinta unidad se refiere al estudio de
                    la programación de dispositivos móviles, la intención de esta unidad es realizar un estudio a nivel
                    introductorio sobre las distintas tecnologías que hay en el mercado, y desarrollar aplicaciones
                    sencillas para esta clase de dispositivos.</p>
