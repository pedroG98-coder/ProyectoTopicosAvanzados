                <p align="justify" class="text">En la parte del módulo de Practicas se desarrollaron actividades fáciles
                    como así complejas, por lo cual se comenzó con el principal “Hola Mundo”, después se desarrollaron
                    practicas más complejas en el cual se maneja los diferentes tipos de eventos que tiene el lenguaje
                    de C# la segunda actividad fue que el botón es donde estará el evento, esto es para que cuando
                    pasemos el puntero del mouse el resultado sea como se muestra: Cuando pasemos el puntero del mouse
                    se mostrará una imagen. En la actividad 3 es para calcular la distancia de un objecto, aplicando la
                    fórmula de D=v*t. En la actividad 4 se creó una lista de imágenes esto es para cada vez que demos
                    clic a una opción, nos refleje la imagen y finalmente en la actividad 5 se desarrolló un programa
                    donde contendrá un panel y se almacenara un aleatorio de imágenes. En la unidad dos como primera
                    actividad haremos la suma, resta, división y multiplicación de dos números utilizando librerías DLL,
                    para la actividad 2 tendremos que resolver una ecuación cuadrática usando librerías. En la actividad
                    3 generaremos un Random de números aleatorios en una lista usando librerías. Como actividad 4
                    haremos un hola mundo desde consola y usando DLL, finalmente como actividad 5 números ordenados
                    aleatoriamente mediante DLL. En la tercera unidad como primera actividad tendremos que crear un
                    programa donde nos permita hacer una cuenta numérica del 1 al 50 y todos se muestre en una lista el
                    segundo botón que te permita mostrar un mensaje y el tercero que genere nuevos botones y al momento
                    de darles clic empiece una cuenta regresiva del 10 al 0 y cuando llegue a 0 que ese botón se
                    deshabilite utilizando Hilos. Por parte de la actividad 2 se realizará un cuadro comparativo de
                    flujo único y flujo múltiple, en la actividad 3 se desarrollará un juego de Galaga el cual te
                    permita tener un personaje que solo se pueda mover de izquierda a derecha y disparar a una nave
                    enemiga utilizando hilos, en la actividad 4 se desarrolla un juego de carrera de autos utilizando
                    hilos y finalmente se desarrollará un programa donde nos permita dibujar círculos azules y rojos
                    mediante hilos. En la cuarta unidad trabajaremos con la conexión, manipulación y visualización de
                    base de datos, el cual se desarrollará un CRUD, en donde podamos insertar, leer, actualizar y
                    eliminar datos de un formulario a una base de datos.</p>
